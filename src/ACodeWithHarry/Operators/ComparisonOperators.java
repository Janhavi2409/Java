package ACodeWithHarry.Operators;

public class ComparisonOperators {

    //comparison operators are ==, >=, <=, >, <

    public static void main(String[] args) {
        System.out.println("Comparison Operators");
        System.out.println("18=19: "+ (18==19));//equal
        System.out.println("21=21: "+ (21==21));//equal
        System.out.println("34>/=34: "+(34>=34));//greater than or equal
        System.out.println("38>22: "+(38>22));//greater than
        System.out.println("49<33: "+(49<33));//less than
        System.out.println("63</=98: "+(63<=98));//less than or equal
        System.out.println("16!=16: "+(16!=16));//is not equal to
    }
}
